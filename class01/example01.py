# 输出，打印日志
print('hello world')

# 逗号，枚举
print('hello world', 'will')

# 等号，赋值
x = 1
print(x)

# 括号，优先级，函数的参数部分，元组
y = (1, 2)
print(type(y))

# 冒号代表一个子过程的开始，缩进代表一个子过程的结束
if x > 1:
    z = 2
    print(z)
else:
    z = 4
    print(z)

# 双引号，单引号都表示一个字符串
s = '今天天气不错'
print(s)
print(type(s))

# 取余数%
x = 5
y = 3
print(x % y)

# 幂运算（指数运算）**
print(2 ** 0.1)

# 整除//
print(5 // 2)
print(5 / 2)

# +-代表正负号，python中没有自增和自减
x = 1
print(++x)
print(+-x)
print(-+x)
print(--x)

# 逻辑运算符，True False 首字母大写
y = 2
print(x > y)
print(x < y)

